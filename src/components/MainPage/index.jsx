function floatToTimeString(floatHour) {
  const hours = Math.floor(floatHour);
  const minutes = Math.round((floatHour - hours) * 60);
  return `${String(hours).padStart(2, "0")}:${String(minutes).padStart(2, "0")}:00`;
}



