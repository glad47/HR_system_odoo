function getDaysInRange(startDateStr, endDateStr, startTime, endTime) {
  const days = [];
  const startDate = new Date(startDateStr);
  const endDate = new Date(endDateStr);

  // Normalize to midnight for iteration
  let current = new Date(startDate);
  let current2 = new Date(startDate + 1);
  while (current <= endDate) {
    const dateKey = current.toISOString().split("T")[0]; // YYYY-MM-DD
    const dateKey2 = current2.toISOString().split("T")[0]; // YYYY-MM-DD
    days.push({
      date: dateKey,
      start: startTime ? `${dateKey}T${startTime}` : null,
      end: endTime ? `${dateKey2}T${endTime}` : null,
    });

    // Move to next day
    current.setDate(current.getDate() + 1);
  }

  return days;
}
  




















  








